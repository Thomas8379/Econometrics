@require_tab("asciiqob.tab")
def test_ak91_sample_integrity() -> None:
    """asciiqob.tab: 329,509 observations, correct columns, no missing values."""
    df = pd.read_csv(_tab("asciiqob.tab"), sep="\t")
    assert len(df) == 329_509
    assert list(df.columns) == [
        "log_weekly_wage", "education", "year_of_birth",
        "quarter_of_birth", "place_of_birth",
    ]
    assert df.isna().sum().sum() == 0
    assert set(df["year_of_birth"].unique()) == set(range(30, 40))
    assert df["quarter_of_birth"].between(1, 4).all()


@require_tab("asciiqob.tab")
def test_ak91_ols_vs_r_fixture() -> None:
    """AK91 OLS: lwklywge ~ yr21-yr29 + educ matches R/ak91.sas benchmark.

    R source: proc syslin, model lwklywge = yr21-yr29 educ
    Tolerance: ±0.5 % on coef, ±2 % on SE.
    """
    import statsmodels.api as sm

    fx = _fixture("ak91")["ols"]
    df = pd.read_csv(_tab("asciiqob.tab"), sep="\t")

    # Mirror R: yr21=(yob==31), ..., yr29=(yob==39); yr20=(yob==30) is reference
    for yr in range(21, 30):
        df[f"yr{yr}"] = (df["year_of_birth"] == yr + 10).astype(float)
    yr_cols = [f"yr{y}" for y in range(21, 30)]

    X = sm.add_constant(pd.concat([df[yr_cols], df[["education"]].astype(float)], axis=1))
    ols = sm.OLS(df["log_weekly_wage"].astype(float), X).fit()

    assert int(ols.nobs) == fx["nobs"]
    assert_coef_close(float(ols.params["education"]), fx["educ_coef"], "AK91 OLS educ")
    assert_se_close(float(ols.bse["education"]), fx["educ_se"], "AK91 OLS educ")


@require_tab("asciiqob.tab")
def test_ak91_iv3_vs_r_fixture() -> None:
    """AK91 2SLS (3 QOB instruments): educ coef matches R/ak91.sas benchmark.

    R source: AER::ivreg, instruments = yr21-yr29 + qtr1 + qtr2 + qtr3
    Tolerance: ±0.5 % on coef, ±2 % on SE.
    """
    from econtools.models.iv import fit_iv_2sls

    fx = _fixture("ak91")["iv3"]
    df = pd.read_csv(_tab("asciiqob.tab"), sep="\t")

    for q in [1, 2, 3, 4]:
        df[f"q{q}"] = (df["quarter_of_birth"] == q).astype(float)
    for yr in range(21, 30):
        df[f"yr{yr}"] = (df["year_of_birth"] == yr + 10).astype(float)
    yr_cols = [f"yr{y}" for y in range(21, 30)]

    result = fit_iv_2sls(
        df,
        dep_var="log_weekly_wage",
        exog_vars=yr_cols,
        endog_vars=["education"],
        instruments=["q1", "q2", "q3"],
        add_constant=True,
    )

    assert result.fit.nobs == fx["nobs"]
    assert_coef_close(float(result.params["education"]), fx["educ_coef"], "AK91 2SLS-3 educ")
    assert_se_close(float(result.bse["education"]), fx["educ_se"], "AK91 2SLS-3 educ")


