def _load_lavy(grade: int) -> pd.DataFrame:
    df = pd.read_csv(_tab(f"final{grade}.tab"), sep="\t", quoting=3)
    df["avgverb"] = df["avgverb"].where(df["avgverb"] <= 100, df["avgverb"] - 100)
    df["avgmath"] = df["avgmath"].where(df["avgmath"] <= 100, df["avgmath"] - 100)
    df["func1"] = df["c_size"] / (((df["c_size"] - 1) / 40).astype(int) + 1)
    df.loc[df["verbsize"] == 0, "avgverb"] = np.nan
    df.loc[df["mathsize"] == 0, "avgmath"] = np.nan
    df.loc[df["verbsize"] == 0, "passverb"] = np.nan
    df.loc[df["mathsize"] == 0, "passmath"] = np.nan
    df = df[(df["classize"] > 1) & (df["classize"] < 45) & (df["c_size"] > 5)]
    df = df[(df["c_leom"] == 1) & (df["c_pik"] < 3)]
    # Table 4 additionally drops rows with missing avgverb (keep if avgverb~=.)
    df = df[~df["avgverb"].isna()]
    return df.copy()


@require_tab("final5.tab")
def test_lavy_grade5_sample_integrity() -> None:
    """AL99 grade-5 sample after Table 4 restrictions: matches R fixture N."""
