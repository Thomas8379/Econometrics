@require_tab("aerdat4.tab")
def test_vouchers_balance_phone_vs_r() -> None:
    """Colombia vouchers Table 3 Panel C: phone ~ vouch0 matches R/SAS benchmark.

    R source: table3_final.sas proc reg; model phone = vouch0 ... /acov
    Expected: vouch0 ≈ 0.020 (small — confirms lottery randomisation).
    """
    import statsmodels.api as sm

    fx = _fixture("vouchers02")["panel_c"]["phone"]
    df = pd.read_csv(_tab("aerdat4.tab"), sep="\t")
    svy = _survey_sample(df)
    controls = _panel_c_controls(svy)

    d = svy.dropna(subset=["phone"] + controls)
    X = sm.add_constant(d[controls].astype(float))
    ols = sm.OLS(d["phone"].astype(float), X).fit(cov_type="HC0")

    assert int(ols.nobs) == fx["nobs"]
    assert_coef_close(float(ols.params["vouch0"]), fx["coef"], "Vouchers phone~vouch0")
    assert_se_close(float(ols.bse["vouch0"]), fx["se"], "Vouchers phone~vouch0 SE")


