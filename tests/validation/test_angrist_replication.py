def _survey_sample(df: pd.DataFrame) -> pd.DataFrame:
    mask = (
        df["bog95smp"].fillna(0).astype(bool)
        | df["bog97smp"].fillna(0).astype(bool)
        | df["jam93smp"].fillna(0).astype(bool)
    )
    return df[mask].copy()


def _panel_c_controls(df: pd.DataFrame) -> list[str]:
    """Return Panel C controls matching R's vouchers02_fixtures.R specification.

    R's lm() aliases seven columns as perfectly collinear (structural duplicates):
      - dbogota  = 1 - djamundi  (every obs is from exactly one city)
      - d1993    = djamundi      (all 1993 survey obs are from Jamundi)
      - d1997    = 1 - d1995 - djamundi  (linear combo of kept cols)
      - dmonth12 = reference month (sum of month dummies = 1 for all survey obs)
      - darea2, darea3, darea13  = all-zero in the survey sample
    We exclude these to ensure statsmodels uses the same rank-33 model as R.
    Column order follows the dataframe (R's grep order), not sorted alphabetically.
    """
    area_cols  = [c for c in df.columns if c.startswith("darea")]
    month_cols = [c for c in df.columns if c.startswith("dmonth")]
    base = ["vouch0", "svy", "hsvisit", "djamundi", "d1995"]
    month_keep = [c for c in month_cols if c != "dmonth12"]
    area_keep  = [c for c in area_cols if c in df.columns and df[c].std() > 0]
    return base + month_keep + area_keep


