"""Numerical validation: Angrist replication datasets against R-generated fixtures.

External ground truth
---------------------
All benchmark values in this file come from JSON fixtures generated by R scripts
in scripts/r/, which are independent translations of Angrist's original Stata/SAS
replication code.  The fixture generation pipeline is:

    Angrist's Stata/SAS code
         ↓  translated to
    scripts/r/{paper}_fixtures.R   (R, using AER::ivreg + sandwich)
         ↓  executed by
    scripts/generate_fixtures.py
         ↓  produces
    tests/validation/fixtures/angrist/{paper}.json   ← loaded here

This means the tests genuinely verify that econtools output agrees with an
independent implementation (R), not just with itself.

To regenerate fixtures after data updates:
    python scripts/generate_fixtures.py --force

Papers covered
--------------
1. Angrist & Krueger (1991) — QJE  (doi:10.7910/DVN/ENLGZX)
   Dataset: asciiqob.tab  (N = 329,509; 1980 Census)
   R source: ak91.sas → scripts/r/ak91_fixtures.R

2. Angrist & Lavy (1999) — QJE  (doi:10.7910/DVN/XRSUJU)
   Datasets: final5.tab, final4.tab
   R source: AngristLavy_Table4.do → scripts/r/lavy99_fixtures.R

3. Angrist, Bettinger, Bloom, King & Kremer (2002) — AER  (doi:10.7910/DVN/K57TOZ)
   Dataset: aerdat4.tab  (N = 1,618 survey respondents)
   R source: table2_final.sas → scripts/r/vouchers02_fixtures.R

Tolerance policy
----------------
- Coefficients: ±0.5 % relative (R and Python use the same LAPACK routines for OLS/2SLS)
- Standard errors: ±2 % relative (small-sample correction choices differ between
  implementations; AER::ivreg default HC vs linearmodels default; cluster SE formulae)
- Sample sizes: exact integer match
"""

from __future__ import annotations

import json
from pathlib import Path

import numpy as np
import pandas as pd
import pytest

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------

_ANGRIST_DIR = (
    Path(__file__).resolve().parents[2]
    / "data_lake" / "raw" / "angrist_replication"
)
_FIXTURE_DIR = Path(__file__).resolve().parent / "fixtures" / "angrist"


def _tab(filename: str) -> Path:
    return _ANGRIST_DIR / filename


def _fixture(name: str) -> dict:
    path = _FIXTURE_DIR / f"{name}.json"
    if not path.exists():
        pytest.skip(
            f"Fixture {path.name} not found. "
            f"Run: python scripts/generate_fixtures.py --paper {name.split('_')[0]}"
        )
    return json.loads(path.read_text(encoding="utf-8"))


def require_tab(filename: str) -> pytest.MarkDecorator:
    path = _tab(filename)
    return pytest.mark.skipif(
        not path.exists(),
        reason=f"data_lake/raw/angrist_replication/{filename} not present",
    )


# ---------------------------------------------------------------------------
# Tolerance helpers
# ---------------------------------------------------------------------------

def assert_coef_close(actual: float, expected: float, label: str, rtol: float = 0.005) -> None:
    """Assert coefficients agree within rtol (relative tolerance)."""
    if abs(expected) < 1e-9:
        assert abs(actual - expected) < 1e-4, (
            f"{label}: actual={actual:.6f} expected={expected:.6f} (near-zero, abs check)"
        )
    else:
        rel_err = abs(actual - expected) / abs(expected)
        assert rel_err <= rtol, (
            f"{label}: actual={actual:.6f} expected={expected:.6f} "
            f"relative error={rel_err:.4%} (limit {rtol:.1%})"
        )


def assert_se_close(actual: float, expected: float, label: str, rtol: float = 0.02) -> None:
    """Assert standard errors agree within rtol (2 % default — allows for small-sample correction differences)."""
    if abs(expected) < 1e-9:
        return
    rel_err = abs(actual - expected) / abs(expected)
    assert rel_err <= rtol, (
        f"{label} SE: actual={actual:.6f} expected={expected:.6f} "
        f"relative error={rel_err:.4%} (limit {rtol:.1%})"
    )


